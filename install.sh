spinner() {
    local pid=$1
    local msg="$2"
    local sp='|/-\\'
    local i=0
    local use_tty=1
    if [ ! -t 1 ]; then use_tty=0; fi
    if [ $use_tty -eq 1 ]; then tput civis 2>/dev/null || true; fi
    while kill -0 $pid 2>/dev/null; do
        if [ $use_tty -eq 1 ]; then
            printf "\r${CYAN}%s ${NC}%s" "$msg" "${sp:i++%4:1}"
        fi
        sleep 0.1
    done
    wait $pid
    local status=$?
    if [ $use_tty -eq 1 ]; then
        tput cnorm 2>/dev/null || true
        if [ $status -eq 0 ]; then
            printf "\r${GREEN}‚úî %s${NC}\n" "$msg"
        else
            printf "\r${RED}‚úñ %s (–æ—à–∏–±–∫–∞)${NC}\n" "$msg"
        fi
    fi
    return $status
}

run_with_spinner() {
    # usage: run_with_spinner "command" "Message"
    bash -c "$1" &
    local pid=$!
    spinner $pid "$2"
    wait $pid
}

